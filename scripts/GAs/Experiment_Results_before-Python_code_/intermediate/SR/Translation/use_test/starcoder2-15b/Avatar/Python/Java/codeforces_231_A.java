public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int c = 0;
        for (int i = 0; i < n; i++) {
            int a = sc.nextInt();
            int b = sc.nextInt();
            int c1 = sc.nextInt();
            if (a + b + c1 > 1) {
                c++;
            }
        }
        System.out.println(c);
    }
}

