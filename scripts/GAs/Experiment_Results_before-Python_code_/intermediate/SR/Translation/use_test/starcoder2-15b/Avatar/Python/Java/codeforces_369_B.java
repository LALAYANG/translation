import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int k = sc.nextInt();
        int l = sc.nextInt();
        int r = sc.nextInt();
        int sa = sc.nextInt();
        int sk = sc.nextInt();
        int[] ans = new int[n];
        for (int i = 0; i < k; i++) {
            if (i < sk % k) {
                ans[i] = sk / k + 1;
            } else {
                ans[i] = sk / k;
            }
        }
        for (int i = k; i < n; i++) {
            if (i < (sa - sk) % (n - k)) {
                ans[i] = (sa - sk) / (n - k) + 1;
            } else {
                ans[i] = (sa - sk) / (n - k);
            }
        }
        for (int i = 0; i < n; i++) {
            System.out.print(ans[i] + " ");
        }
    }
}

