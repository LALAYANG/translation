import java.util.*;
import java.io.*;
public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String s = sc.next();
        if(s.length() == 1){
            System.out.println(0);
            return;
        }
        int[] a = new int[2];
        for(int i = 0; i < s.length(); i++){
            if(s.charAt(i) == '0'){
                a[0]++;
            }else{
                a[1]++;
            }
        }
        int ans = Math.min(a[0], a[1]) * 2;
        System.out.println(ans);
    }
}

