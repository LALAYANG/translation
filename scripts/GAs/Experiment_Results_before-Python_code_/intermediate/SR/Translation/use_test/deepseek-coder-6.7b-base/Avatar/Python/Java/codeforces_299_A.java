import java.util.*;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int[] arr = new int[n];
        for (int i = 0; i < n; i++) {
            arr[i] = sc.nextInt();
        }
        Arrays.sort(arr);
        int min = arr[0];
        boolean flag = false;
        for (int i = 1; i < n; i++) {
            if (arr[i] % min != 0) {
                flag = true;
                break;
            }
        }
        if (flag) {
            System.out.println(-1);
        } else {
            System.out.println(min);
        }
    }
}

