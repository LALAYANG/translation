import java.util.Scanner;
import java.util.HashMap;

public class Main {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int N = scanner.nextInt();
        int M = scanner.nextInt();
        int[] A = new int[N];
        for (int i = 0; i < N; i++) {
            A[i] = scanner.nextInt();
        }
        HashMap<Integer, Integer> remainderCounts = new HashMap<>();
        int cumulativeRemainder = 0;
        for (int i = 0; i < N; i++) {
            cumulativeRemainder = (cumulativeRemainder + A[i]) % M;
            int count = remainderCounts.getOrDefault(cumulativeRemainder, 0);
            remainderCounts.put(cumulativeRemainder, count + 1);
        }
        long combinations = 0;
        for (int count : remainderCounts.values()) {
            combinations += (long) count * (count - 1) / 2;
        }
        System.out.println(combinations);
    }
}

