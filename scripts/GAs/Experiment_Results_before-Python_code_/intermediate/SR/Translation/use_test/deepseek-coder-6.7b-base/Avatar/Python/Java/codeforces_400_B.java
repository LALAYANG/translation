import java.util.*;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int m = sc.nextInt();
        String[] arr = new String[n];
        for (int i = 0; i < n; i++) {
            arr[i] = sc.next();
        }
        int[] c = new int[n];
        for (int i = 0; i < n; i++) {
            c[i] = arr[i].indexOf("S") - arr[i].indexOf("G");
        }
        int count = 0;
        for (int i = 0; i < n; i++) {
            if (c[i] < 0) {
                count = -1;
                break;
            }
        }
        if (count != -1) {
            count = c.length;
        }
        System.out.println(count);
    }
}

