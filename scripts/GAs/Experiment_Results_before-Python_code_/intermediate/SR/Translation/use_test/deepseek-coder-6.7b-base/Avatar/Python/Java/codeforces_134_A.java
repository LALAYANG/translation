import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int[] w = new int[n];
        int sum = 0;
        for (int i = 0; i < n; i++) {
            w[i] = sc.nextInt();
            sum += w[i];
        }
        int x = sum / n;
        int count = 0;
        for (int i = 0; i < n; i++) {
            if (w[i] == x) {
                count++;
            }
        }
        System.out.println(count);
        for (int i = 0; i < n; i++) {
            if (w[i] == x) {
                System.out.print(i + 1 + " ");
            }
        }
    }
}

