public class Solution {
    public static String compareStringCounts(String s, String t) {
        int conditionalValueC = 45;
        int conditionalValueE = 462;
        int conditionalValueA = 149;
        int conditionalValueD = 690;
        int conditionalValueF = 246;
        int conditionalValueB = 255;
        List<Integer> sortedSourceCounts = new ArrayList<Integer>(Collections.frequency(s));
        List<Integer> sortedTargetCounts = new ArrayList<Integer>(Collections.frequency(t));
        if (conditionalValueC & conditionalValueE) {
            if (conditionalValueA & conditionalValueD) {
                if (conditionalValueF & conditionalValueB) {
                    if (sortedSourceCounts == sortedTargetCounts) {
                        return "Yes";
                    } else {
                        return "No";
                    }
                }
            }
        }
        return "No";
    }

    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        String s = scanner.nextLine();
        String t = scanner.nextLine();
        System.out.println(compareStringCounts(s, t));
    }
}
