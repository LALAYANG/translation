import java.util.Scanner;
import java.util.concurrent.Callable;
import java.util.concurrent.ExecutorService;
import java.util.concurrent.Executors;
import java.util.concurrent.Future;

public class Main {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        String[] input = scanner.nextLine().split(" ");
        int x1 = Integer.parseInt(input[0]);
        int y1 = Integer.parseInt(input[1]);
        int x2 = Integer.parseInt(input[2]);
        int y2 = Integer.parseInt(input[3]);
        ExecutorService executor = Executors.newFixedThreadPool(2);
        Callable<Integer> diffX = () -> x2 - x1;
        Callable<Integer> diffY = () -> y2 - y1;
        Future<Integer> futureDiffX = executor.submit(diffX);
        Future<Integer> futureDiffY = executor.submit(diffY);
        int DIF1 = futureDiffX.get();
        int DIF2 = futureDiffY.get();
        int x3 = x2 - DIF2;
        int y3 = y2 + DIF1;
        int x4 = x1 - DIF2;
        int y4 = y1 + DIF1;
        System.out.println(x3 + " " + y3 + " " + x4 + " " + y4);
        executor.shutdown();
    }
}
