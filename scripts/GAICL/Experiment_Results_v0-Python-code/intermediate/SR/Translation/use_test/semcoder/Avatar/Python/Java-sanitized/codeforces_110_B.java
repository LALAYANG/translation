import java.util.Scanner;

public class codeforces_110_B {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int n = scanner.nextInt();
        String s = "";

        for (int i = 0; i < n; i++) {
            s += "abcd";
        }

        System.out.println(s);
    }
}